import { spawnSync } from "node:child_process";
import { existsSync, mkdirSync, renameSync, writeFileSync } from "node:fs";
import path from "node:path";
import process from "node:process";
import { pathToFileURL } from "node:url";

import {
  LOCAL_GROBID_URL,
  ROOT,
  isLocalSupabaseUrl,
  parseEnvBlock,
  readEnvFile,
} from "./local-stack-lib.mjs";

function run(command, args) {
  const result = spawnSync(command, args, {
    cwd: ROOT,
    encoding: "utf8",
    stdio: "pipe",
  });

  if (result.status !== 0) {
    throw new Error(
      result.stderr?.trim() ||
        `${command} ${args.join(" ")} failed with exit code ${result.status ?? 1}.`,
    );
  }

  return (result.stdout ?? "").trim();
}

function readSupabaseStatusEnv() {
  return parseEnvBlock(run("supabase", ["status", "-o", "env"]));
}

function assertStackEnv(stackEnv) {
  const missing = [
    "API_URL",
    "ANON_KEY",
    "SERVICE_ROLE_KEY",
    "DATABASE_URL",
  ].filter((key) => !stackEnv[key]);

  if (missing.length > 0) {
    throw new Error(
      `Supabase status is missing ${missing.join(", ")}. Run \`pnpm dev:services\` first.`,
    );
  }
}

function isRemoteLookingEnv(env) {
  const supabaseUrl = env?.NEXT_PUBLIC_SUPABASE_URL;
  return Boolean(supabaseUrl && !isLocalSupabaseUrl(supabaseUrl));
}

function nextBackupPath(filePath) {
  const stamp = new Date().toISOString().replace(/[:.]/g, "-");
  const basePath = `${filePath}.remote-backup-${stamp}`;
  let candidate = basePath;
  let counter = 1;

  while (existsSync(candidate)) {
    candidate = `${basePath}-${counter}`;
    counter += 1;
  }

  return candidate;
}

function backupRemoteEnv(relativePath) {
  const env = readEnvFile(relativePath);
  const filePath = path.join(ROOT, relativePath);

  if (!env || !existsSync(filePath) || !isRemoteLookingEnv(env)) {
    return null;
  }

  const backupPath = nextBackupPath(filePath);
  renameSync(filePath, backupPath);
  return path.relative(ROOT, backupPath);
}

function writeEnvFile(relativePath, content) {
  const filePath = path.join(ROOT, relativePath);
  mkdirSync(path.dirname(filePath), { recursive: true });
  writeFileSync(filePath, content, { encoding: "utf8", mode: 0o600 });
}

export function syncLocalEnv() {
  const stackEnv = readSupabaseStatusEnv();
  assertStackEnv(stackEnv);

  const backups = [
    backupRemoteEnv("apps/web/.env.local"),
    backupRemoteEnv("apps/worker/.env.local"),
    backupRemoteEnv("apps/worker/.env"),
  ].filter(Boolean);

  writeEnvFile(
    "apps/web/.env.local",
    [
      "# Generated by pnpm dev:local:sync-env. Do not commit.",
      `NEXT_PUBLIC_SUPABASE_URL=${stackEnv.API_URL}`,
      `NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY=${stackEnv.ANON_KEY}`,
      `SUPABASE_SERVICE_ROLE_KEY=${stackEnv.SERVICE_ROLE_KEY}`,
      `DATABASE_URL=${stackEnv.DATABASE_URL}`,
      "SUPABASE_ORCID_PROVIDER=custom:orcid",
      "",
    ].join("\n"),
  );

  writeEnvFile(
    "apps/worker/.env.local",
    [
      "# Generated by pnpm dev:local:sync-env. Do not commit.",
      `DATABASE_URL=${stackEnv.DATABASE_URL}`,
      `NEXT_PUBLIC_SUPABASE_URL=${stackEnv.API_URL}`,
      `SUPABASE_SERVICE_ROLE_KEY=${stackEnv.SERVICE_ROLE_KEY}`,
      `GROBID_URL=${LOCAL_GROBID_URL}`,
      "",
    ].join("\n"),
  );

  return {
    backups,
    apiUrl: stackEnv.API_URL,
    studioUrl: stackEnv.STUDIO_URL,
  };
}

if (import.meta.url === pathToFileURL(process.argv[1] ?? "").href) {
  try {
    const result = syncLocalEnv();
    console.log("Local env files synced.");
    console.log("- apps/web/.env.local");
    console.log("- apps/worker/.env.local");
    if (result.backups.length > 0) {
      console.log("Backed up remote-looking env files:");
      for (const backup of result.backups) {
        console.log(`- ${backup}`);
      }
    }
  } catch (error) {
    console.error(
      error instanceof Error
        ? error.message
        : "Unable to sync local env files.",
    );
    process.exitCode = 1;
  }
}
